public class LatestOffer {

    private String offerId;
    private String offerTittel;
    private String offerDescription;
    private String offerBanner;


    public String getOfferId() {
        return offerId;
    }

    public void setOfferId(String offerId) {
        this.offerId = offerId;
    }

    public String getOfferTittel() {
        return offerTittel;
    }

    public void setOfferTittel(String offerTittel) {
        this.offerTittel = offerTittel;
    }

    public String getOfferDescription() {
        return offerDescription;
    }

    public void setOfferDescription(String offerDescription) {
        this.offerDescription = offerDescription;
    }

    public String getOfferBanner() {
        return offerBanner;
    }

    public void setOfferBanner(String offerBanner) {
        this.offerBanner = offerBanner;
    }
}
