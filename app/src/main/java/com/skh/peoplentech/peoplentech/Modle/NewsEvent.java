public class NewsEvent {

    private  String ne_id;
    private  String ne_name;
    private  String ne_details;
    private  String ne_link;
    private  String ne_img;

    public String getNe_id() {
        return ne_id;
    }

    public void setNe_id(String ne_id) {
        this.ne_id = ne_id;
    }

    public String getNe_name() {
        return ne_name;
    }

    public void setNe_name(String ne_name) {
        this.ne_name = ne_name;
    }

    public String getNe_details() {
        return ne_details;
    }

    public void setNe_details(String ne_details) {
        this.ne_details = ne_details;
    }

    public String getNe_link() {
        return ne_link;
    }

    public void setNe_link(String ne_link) {
        this.ne_link = ne_link;
    }

    public String getNe_img() {
        return ne_img;
    }

    public void setNe_img(String ne_img) {
        this.ne_img = ne_img;
    }
}
